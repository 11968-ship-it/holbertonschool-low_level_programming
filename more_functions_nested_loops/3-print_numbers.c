/**
 * print_numbers - Prints the numbers, from 0 to 9, followed by a new line
 * @c: The character to check (as an ASCII value)
 *
 * Description: Tret
 * Return: ise
 */

void print_numbers(void)
{
char str[] = "_putchar";
int i;
for (i = 0; str[i] != '\0'; i++)
{
_putchar(str[i]);
}
_putchar('\n');
return (0);
}
